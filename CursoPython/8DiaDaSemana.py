# Programa que verifica qual o dia da semana.

numero_dia = int(input("Digite um número: "))

if numero_dia == 1:
    print("Domingo")

elif numero_dia == 2:
    print("Segunda-Feira")

elif numero_dia == 3:
    print("Terça-feira")


elif numero_dia == 4:
    print("Quarta-feira")

elif numero_dia == 5:
    print("Quinta-feira")

elif numero_dia == 6:
    print("Sexta-feira")

elif numero_dia == 7:
    print("Sabado")

else:
    print("Numero inválido")