# Criando uma variavel lista

frutas = ["maça","banana","laranja","manga","uva"]

# Trazendo o elemento laranja que esta no index 2.
print(frutas[2])


# Adicionando novos valores na lista
frutas.append("abacaxi")
print(frutas)

# Removendo elementos da lista
frutas.remove("maça")
print(frutas)

# Contando os elementos de uma lista
print(frutas.count("banana"))